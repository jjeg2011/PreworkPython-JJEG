'''
Ejercicio 7: Calculadora Simple
 Crea un programa que realice operaciones aritméticas simples (suma, resta, 
multiplicación, división) según la elección del usuario.
'''
def suma (a, b):
  return a + b
def resta (a, b):
  return a - b
def multiplica (a, b):
  return a * b
def division (a, b):
  return a / b
print(division(4, 15))

